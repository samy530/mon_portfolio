// --- Variables de filtre --- //
let filtreCategorie = "tous";
let filtreNiveau = "tous";
let rechercheTexte = "";

// --- Affichage --- //
function afficherResumes() {
  const container = document.getElementById("resumes-container");
  container.innerHTML = "";

  const filtres = resumes.filter(r => {
    const categorieOK = (filtreCategorie === "tous" || r.categorie === filtreCategorie);
    const niveauOK = (filtreNiveau === "tous" || r.niveau === filtreNiveau);
    const rechercheOK = r.titre.toLowerCase().includes(rechercheTexte.toLowerCase());
    return categorieOK && niveauOK && rechercheOK;
  });

  if (filtres.length === 0) {
    container.innerHTML = "<p>Aucun résumé trouvé.</p>";
    return;
  }

  filtres.forEach(r => {
    const carte = document.createElement("div");
    carte.className = "resume-carte";
    carte.innerHTML = `
      <h3>${r.titre}</h3>
      <div class="resume-actions">
        <a href="${r.fichier}" target="_blank" class="btn">Ouvrir PDF</a>
      </div>
    `;
    container.appendChild(carte);
  });
}

// --- Filtres --- //
function filtrerCategorie(categorie) {
  filtreCategorie = categorie;
  afficherResumes();
}

function filtrerNiveau(niveau) {
  filtreNiveau = niveau;
  afficherResumes();
}

document.getElementById("recherche").addEventListener("input", (e) => {
  rechercheTexte = e.target.value;
  afficherResumes();
});

// --- Initialisation --- //
window.onload = afficherResumes;
